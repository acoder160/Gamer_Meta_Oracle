from pydantic import BaseModel, Field
from typing import List

class GameContext(BaseModel):
    """Contextual information about the game and its state."""
    target_subject: str = Field(description="Specific character, item or mechanic")
    game_origin: str = Field(description="Country or region where the game was developed (e.g., RU, CN, JP, US)")
    baseline_version: str = Field(description="The version/patch used as a comparison point")
    current_version: str = Field(description="The latest known or estimated version")

class SearchPlan(BaseModel):
    """Structured queries for different research layers."""
    official: List[str] = Field(description="Queries for patch notes and developer blogs")
    pro_statistics: List[str] = Field(description="Queries for winrates and high-level stats")
    high_tier_community: List[str] = Field(description="Queries for pro-player discussions and niche forums")

class RouterResponse(BaseModel):
    """The final plan generated by the Router Agent."""
    game_name: str = Field(description="Full name of the game")
    query_language: str = Field(description="The language used by the user")
    intent_type: str = Field(description="Category of intent (e.g., 'build', 'macro', 'technical')")
    context: GameContext
    search_plan: SearchPlan

class FinalSynthesis(BaseModel):
    """The final structured output containing the guide and metadata."""
    verified_version: str = Field(description="The actual game patch/version found in the text (e.g., '17.0' or 'Unknown')")
    confidence_score: int = Field(description="Score from 0 to 100 based on the quality and relevance of the scraped sources")
    markdown_guide: str = Field(description="A beautifully formatted Markdown guide answering the user's query")